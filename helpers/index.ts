function capitalizeWord([first, ...rest]: any, lc: any) {
  return (
    first.toUpperCase() + (lc ? rest.join("").toLowerCase() : rest.join(""))
  );
}

export function capitalize(str: String, lc = true, all = true) {
  if (str.includes(".")) {
    return str.toUpperCase();
  }

  return all
    ? str
        .split(/(\s|-|')/)
        .map((s) => capitalizeWord(s, lc))
        .join("")
    : capitalizeWord(str, lc);
}

export function capitalizeSpecialCharacters(str: string, sc: string) {
  return str
    .split(sc)
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join(sc);
}
